def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--smoke", action="store_true", default=False)
    ap.add_argument("--bench", action="store_true", default=False)
    ap.add_argument("--export-cubin", action="store_true", default=False)
    ap.add_argument("--csv-out", default="results.csv")
    ap.add_argument("--cubin-out", default="gdn_decode_fused.cubin")
    # Wave 22.15 — shape sweep. Default keeps the legacy headline behaviour
    # (qwen3_next_decode); pass --shape NAME to bench any of the registry
    # shapes (qwen3_next_decode, tiny, small, large, wide).
    ap.add_argument(
        "--shape", default=SHAPE_QWEN3_NEXT_DECODE.name,
        choices=sorted(SHAPE_REGISTRY.keys()),
        help="Bench shape (default: qwen3_next_decode).",
    )
    args = ap.parse_args()

    if not (args.smoke or args.bench or args.export_cubin):
        args.smoke = True

    print(f"cuda-tile version: {ct.__version__}")
    print(f"cupy: {cupy.__version__}")
    props = cupy.cuda.runtime.getDeviceProperties(0)
    print(f"device: {props['name'].decode()}")
    print(f"compute capability: sm_{props['major']}{props['minor']}")
    print()

    bench_shape = SHAPE_REGISTRY[args.shape]

    rc = 0
    if args.smoke:
        ok = run_smoke(SHAPE_CORRECTNESS)
        if not ok:
            rc = 1
        print()

    if args.bench:
        run_bench(bench_shape, args.csv_out)
        print()

    if args.export_cubin:
        print(f"Exporting cubin at shape={bench_shape.name} for SASS inspection…")
        export_cubin(args.cubin_out, bench_shape)

    return rc


if __name__ == "__main__":
    sys.exit(main())
