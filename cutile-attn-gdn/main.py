def load_inputs(shape: GDNShape):
    prefix = INPUTS_DIR / f"gdn_{shape.name}"
    return {
        "q": np.load(f"{prefix}_q_f16.npy"),
        "k": np.load(f"{prefix}_k_f16.npy"),
        "v": np.load(f"{prefix}_v_f16.npy"),
        "alpha": np.load(f"{prefix}_alpha_f16.npy"),
        "beta": np.load(f"{prefix}_beta_f16.npy"),
        "S_in": np.load(f"{prefix}_S_in_f32.npy"),
        "o_expected": np.load(f"{prefix}_o_expected_f16.npy"),
        "S_out_expected": np.load(f"{prefix}_S_out_expected_f32.npy"),
    }


def prepare_device(shape: GDNShape, inp: dict):
    B, H, d_k, d_v = shape.batch, shape.n_heads, shape.d_k, shape.d_v
    BH = B * H

    q_d = cupy.asarray(inp["q"].reshape(BH, d_k), dtype=cupy.float16)
    k_d = cupy.asarray(inp["k"].reshape(BH, d_k), dtype=cupy.float16)
    v_d = cupy.asarray(inp["v"].reshape(BH, d_v), dtype=cupy.float16)
    alpha_d = cupy.asarray(inp["alpha"].reshape(BH, 1), dtype=cupy.float16)
    beta_d = cupy.asarray(inp["beta"].reshape(BH, 1), dtype=cupy.float16)
    # State: (B, H, d_k, d_v) → (B*H*d_k, d_v)
    s_in_d = cupy.asarray(inp["S_in"].reshape(BH * d_k, d_v), dtype=cupy.float32)
    s_out_d = cupy.zeros((BH * d_k, d_v), dtype=cupy.float32)
    o_d = cupy.zeros((BH, d_v), dtype=cupy.float16)
    return q_d, k_d, v_d, alpha_d, beta_d, s_in_d, s_out_d, o_d


# ─────────────────────────────────────────────────────────────────────────────
# Block-size picker
# ─────────────────────────────────────────────────────────────────────────────


def pick_block_v(shape: GDNShape) -> int:
    """Pick BLOCK_V given the per-block state tile budget.

    Constraint: tile size D_K · BLOCK_V · 4 bytes must fit in a single
    shared-memory allocation (~192 KB safe on sm_120) but we want the
    tile to be matmul-friendly (multiple of 8 or 16).

    For d_k=64: BV=32 → 8 KB tile (tiny, fine).
    For d_k=256: BV=64 → 64 KB tile (fits comfortably).
    For d_k=512: BV=32 → 64 KB tile (`wide` shape; bigger BV would
    blow past the dynamic-smem cap on sm_120).
    """
    # Conservative: keep state tile ≤ 64 KB f32 = 16384 elements.
    max_elems = 16384
    # Must divide d_v evenly.
    for bv in (128, 64, 32, 16, 8):
        if shape.d_v % bv == 0 and shape.d_k * bv <= max_elems:
            return bv
    raise ValueError(f"no feasible BLOCK_V for d_k={shape.d_k} d_v={shape.d_v}")


