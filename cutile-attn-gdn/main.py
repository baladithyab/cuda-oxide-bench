"""Wave 16.4 — cuTile fused GDN (Gated DeltaNet) single-timestep decode.

Implements the GDN recurrent state update + output read in a single
@ct.kernel. For each (batch, head, d_v-block) grid cell:

    Load q[0:d_k], k[0:d_k], v[bv:bv+BV], alpha, beta   (f16)
    Load S_in[0:d_k, bv:bv+BV]                          (f32)
    Compute:
        u        = k^T · (α · S_in)                     (BV,)  f32
        residual = v - u                                (BV,)  f32
        S_out    = α·S_in  +  β · k ⊗ residual          (d_k, BV)  f32
        o        = q^T · S_out                          (BV,)  f32
    Store S_out, o

Grid: (batch * n_heads, d_v / BLOCK_V). No cross-block reductions —
each block owns its disjoint slice of d_v columns.

Per-head state tile memory:
  - d_k=64, BV=32:   64 · 32 · 4 = 8 KB   (correctness shape)
  - d_k=256, BV=64:  256 · 64 · 4 = 64 KB (Qwen3-Next decode shape)

The state tile is large; cuTile will probably spill through shared
memory (a single 256×64 f32 tile is right at the single-block shared-
memory budget on sm_120). Pick BV to keep the tile reasonable; a
smaller BV means more grid cells but smaller per-block state tile.

CLI:
    --smoke     correctness at SHAPE_CORRECTNESS
    --bench     timed at SHAPE_QWEN3_NEXT_DECODE (1 warmup + 50 iters)
    --csv-out FILE    bench CSV
    --export-cubin    write cubin for SASS inspection
"""
